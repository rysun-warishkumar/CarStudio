// Helper function to get button style with optional disabled state
export const getButtonStyle = (type = 'primary', disabled = false) => {
  if (disabled && type === 'primary') {
    return buttonStyles.primaryDisabled;
  }
  return buttonStyles[type] || buttonStyles.primary;
};
